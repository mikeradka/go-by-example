package main

import "fmt"

func main() {
	fmt.Println("go" + "lang") // string

	fmt.Println("1 + 1 =", 1+1)         // integer
	fmt.Println("7.0 / 3.0 =", 7.0/3.0) // float

	fmt.Println("true && false =", true && false) // boolean operators
	fmt.Println("true || false =", true || false)
	fmt.Println("!true =", !true)
}
